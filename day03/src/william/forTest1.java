package william;

public class forTest1 {
    public static void main(String[] args) {
        //正着打印1-5
        for(int i =1;i <= 5;i++){
            System.out.println(i);
        }
        //反着打印5-1
        for(int j = 5; j >=1; j--){
            System.out.println(j);
        }
    }
}
